let dia = prompt ("Introduce un numero:");

if (dia == 1) {
    console.log ("es lunes");
} else if (dia == 2) {
    console.log ("es martes");
} else if (dia == 3) {
    console.log ("es miercoles");
} else if (dia == 4) {
    console.log ("es jueves");
} else if (dia == 5) {
    console.log ("es viernes");
} else if (dia == 6) {
    console.log ("es sabado");
} else if (dia == 7) {
    console.log ("es domingo");
} else {
    console.log ("no hay dia con ese numero")
}